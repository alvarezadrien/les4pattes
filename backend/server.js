const express = require('express');
const mongoose = require('mongoose');
const cors = require('cors');
require('dotenv').config();
const path = require('path');

// ‚úÖ Imports des routes
const animalRoutes = require('./routes/animalRoutes');
const authRoutes = require('./routes/authRoutes');
const commentRoutes = require('./routes/commentRoutes');
const adoptionRoutes = require('./routes/adoptionRoutes');
const adoptionRequestRoutes = require('./routes/adoption_requestRoutes');
const passwordRoutes = require('./routes/passwordRoutes');

const app = express();
const PORT = process.env.PORT || 5000;

// ‚úÖ Connexion √† la base de donn√©es
const mongoURI =
    process.env.NODE_ENV === 'production'
        ? process.env.MONGO_URI
        : process.env.LOCAL_MONGO_URI;

// ‚úÖ Middlewares globaux
app.use(cors());
app.use(express.json());

// ‚úÖ Sert les images upload√©es (chiens, chats, etc.)
app.use('/uploads', express.static(path.join(__dirname, 'uploads')));

// ‚úÖ Sert les avatars pr√©d√©finis du dossier public/img
app.use('/img', express.static(path.join(__dirname, 'public/img')));

// ‚úÖ Log en console les acc√®s aux images pour debugging (facultatif mais utile)
app.use('/uploads', (req, res, next) => {
    console.log('üñºÔ∏è Image demand√©e :', req.originalUrl);
    next();
});

// ‚úÖ D√©finition des routes API
app.use('/api/animaux', animalRoutes);
app.use('/api/auth', authRoutes);
app.use('/api/comments', commentRoutes);
app.use('/api/adoptions', adoptionRoutes);
app.use('/api/adoptionRequests', adoptionRequestRoutes);
app.use('/api/password', passwordRoutes);

// ‚úÖ Connexion MongoDB et lancement du serveur
mongoose.connect(mongoURI)
    .then(() => {
        console.log('‚úÖ Connexion √† MongoDB r√©ussie');
        app.listen(PORT, () => {
            console.log(`üöÄ Serveur lanc√© sur le port ${PORT}`);
        });
    })
    .catch((err) => {
        console.error('‚ùå √âchec de connexion √† MongoDB :', err.message);
        process.exit(1);
    });
